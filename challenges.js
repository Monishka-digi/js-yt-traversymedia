let x=Math.floor(Math.random()*100+1)
let y=Math.floor(Math.random()*50+1)

//sum
let add= x+y
let addOutput= `${x} + ${y} = ${add}`
console.log(addOutput)

//difference
let diff= x-y
let diffOutput= `${x} - ${y} = ${diff}`
console.log(diffOutput)

//multiplication
let multiplication= x*y
let multiplicationOutput= `${x} * ${y} = ${multiplication}`
console.log(multiplicationOutput)

//division
let division= x/y
let divisionOutput= `${x} / ${y} = ${division}`
console.log(divisionOutput)

//modulus
let modulus= x%y
let modulusOutput= `${x} % ${y} = ${modulus}`
console.log(modulusOutput)